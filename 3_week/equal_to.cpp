#include <iostream>
using namespace std;

int main() {
    int a = 4;

    if (a < 5) {
        cout << a << " is lesser than 5" << endl;
    }

    if (a == 4) {
        cout << a << " is equal to 4" << endl;
    }

    if (a = 5) {
        cout << a << " is equal to 5" << endl;
    }

    if (a == 5) {
        cout << a << " is equal to 5" << endl;
    }
}
